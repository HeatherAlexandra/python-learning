#* CONDICIONALES
#Las condiciones (if-elif-else) permiten ejecutar código 
# según se cumplan ciertas condiciones.

# Ejemplo 
edad = 18

if edad < 18:
    print("Eres menor de edad.")
elif edad == 18:
    print("Tienes 18 años.")
else:
    print("Eres mayor de edad.")


#Python usa indentación para definir bloques, en lugar de {} como en otros lenguajes.
#La indentación es de 4 espacios por convención.
#La palabra clave elif es una abreviatura de "else if".